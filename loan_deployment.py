import streamlit as st
import pandas as pd
import numpy as np
import joblib

# Load model and encoder
model = joblib.load("Loan_prediction_xgb_model (1).pkl")
encoder = joblib.load("label_encoder_Loan_prediction.pkl")

st.title("Loan Prediction System")

st.write("Enter Applicant Details")

# -----------------------------
# User Inputs
# -----------------------------

gender = st.selectbox("Gender", encoder["Gender"].classes_)
married = st.selectbox("Married", encoder["Married"].classes_)
dependents = st.selectbox("Dependents", encoder["Dependents"].classes_)
education = st.selectbox("Education", encoder["Education"].classes_)
self_employed = st.selectbox("Self Employed", encoder["Self_Employed"].classes_)

applicant_income = st.number_input("Applicant Income", min_value=0)
coapplicant_income = st.number_input("Coapplicant Income", min_value=0)

loan_amount = st.number_input("Loan Amount", min_value=0)

loan_term = st.number_input("Loan Amount Term", min_value=0)

credit_history = st.selectbox("Credit History", [0,1])

property_area = st.selectbox("Property Area", encoder["Property_Area"].classes_)

loan_status = st.selectbox("Loan Status", encoder["Loan_Status"].classes_)

# -----------------------------
# Log Transformation
# -----------------------------

loan_amount_log = np.log(loan_amount + 1)

# -----------------------------
# Prediction
# -----------------------------
if st.button("Predict Loan Amount"):

    input_df = pd.DataFrame({
        "Gender":[gender],
        "Married":[married],
        "Dependents":[dependents],
        "Education":[education],
        "Self_Employed":[self_employed],
        "ApplicantIncome":[applicant_income],
        "CoapplicantIncome":[coapplicant_income],
        "Loan_Amount_Term":[loan_term],
        "Credit_History":[credit_history],
        "Property_Area":[property_area],
        "Loan_Status":[loan_status],
        "LoanAmount_log":[loan_amount_log]
    })

    # Encode categorical columns
    for col in encoder:
        if col in input_df.columns:
            input_df[col] = encoder[col].transform(input_df[col])

    input_df = input_df.astype(float)

    # Correct column order
    input_df = input_df[model.get_booster().feature_names]

    prediction = model.predict(input_df)[0]

    # Show predicted loan amount
    st.success(f"Predicted Loan Amount: {prediction:.2f}")

    # -----------------------------
    # Loan Approval Logic
    # -----------------------------

    if credit_history == 1 and applicant_income > 2500:
        st.success("Loan Likely Approved ✅")
    else:
        st.error("Loan Likely Not Approved ❌")

